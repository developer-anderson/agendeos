Política de Privacidade do Aplicativo

Agradecemos por escolher utilizar nosso aplicativo! Esta política de privacidade tem como objetivo explicar como coletamos, usamos, armazenamos e protegemos as informações pessoais dos usuários, incluindo dados como login e senha, endereço, telefone e e-mail. É importante que você leia atentamente este documento antes de fornecer seus dados pessoais.

1. Coleta de Informações

1.1 Informações de Cadastro: Para utilizar nosso aplicativo, é necessário criar uma conta. Durante o processo de cadastro, coletamos informações como seu nome, endereço, telefone e e-mail. Essas informações são necessárias para fornecer acesso aos recursos e serviços disponíveis em nosso aplicativo.

1.2 Informações de Login: Para garantir a segurança de sua conta, coletamos e armazenamos seu login e senha. Essas informações são criptografadas e protegidas para evitar acesso não autorizado.

2. Uso das Informações

2.1 Fornecimento de Serviços: Utilizamos suas informações pessoais para fornecer os serviços e recursos do aplicativo. Isso inclui processar transações, gerenciar pedidos, fornecer suporte ao cliente e personalizar sua experiência de uso.

2.2 Comunicações: Podemos utilizar suas informações de contato, como e-mail ou telefone, para enviar comunicações relacionadas ao uso do aplicativo, como confirmações de pedidos, atualizações de produtos ou informações sobre novos recursos. Você pode optar por não receber essas comunicações a qualquer momento, seguindo as instruções fornecidas no próprio aplicativo.

2.3 Melhorias e Pesquisas: Podemos utilizar informações coletadas para aprimorar nossos serviços, realizar pesquisas de mercado e analisar tendências de uso. Esses dados são tratados de forma agregada e anônima, garantindo a privacidade de sua identidade pessoal.

3. Compartilhamento de Informações

3.1 Terceiros de Confiança: Podemos compartilhar suas informações com terceiros confiáveis que nos auxiliam na prestação de serviços, como processamento de pagamentos, envio de produtos ou fornecimento de suporte técnico. Esses terceiros têm acesso limitado às suas informações pessoais e são obrigados a mantê-las confidenciais.

3.2 Requisitos Legais: Em certas circunstâncias, podemos ser obrigados a divulgar suas informações pessoais em resposta a solicitações legais, como ordens judiciais, processos legais ou para cumprir requisitos regulatórios.

4. Segurança das Informações

Adotamos medidas de segurança apropriadas para proteger suas informações pessoais contra acesso não autorizado, uso indevido, alteração ou destruição. Utilizamos criptografia e protocolos seguros para garantir a integridade e confidencialidade de suas informações.

5. Seus Direitos

Você tem o direito de acessar, corrigir ou excluir suas informações pessoais a qualquer momento. Além disso, você pode optar por revogar o consentimento para o uso de suas informações pessoais, bem como para o recebimento de comunicações do aplicativo.

6. Alterações nesta Política de Privacidade

Podemos atualizar esta política de privacidade ocasionalmente para refletir alter

ações em nossas práticas de privacidade. Recomendamos que você revise esta política regularmente. Quaisquer alterações significativas serão comunicadas a você por meio de notificações no aplicativo ou por outros meios apropriados.

Se você tiver alguma dúvida ou preocupação sobre esta política de privacidade ou sobre o uso de suas informações pessoais, entre em contato conosco por meio dos canais de suporte fornecidos no aplicativo.

